/* A literal is a literal representation of a value on JS. It is a fixed value, not a variable. They are literally provided in the code.

- a variable is not a literal, the information assigned to the variable is the literal
ex: 
(keyword)             (value/literal) hard-code value we are providing.
let     placeholder = 'text';
        (variable)
*/ 

// STRING LITERAL
let city = 'Indy';

// NUMERIC LITERAL
let num = 123;

// Boolean
let isOn = true;

// Array Literal
let daysOfWeek = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday'];

// Object Literal
let movie = {
    title: "elf",
    rating: 'pg',
    release: '2003'

};
